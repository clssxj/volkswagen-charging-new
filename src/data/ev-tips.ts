/**
 * 随机获取一条tip
 */
export function getRandomTip(): string {
  const randomIndex = Math.floor(Math.random() * evTips.length)
  return evTips[randomIndex]
}

/**
 * 获取不重复的随机tip
 */
export function getRandomTipExcept(currentTip: string): string {
  let newTip = getRandomTip()
  let attempts = 0
  
  // 避免连续显示相同的tip，最多尝试10次
  while (newTip === currentTip && attempts < 10) {
    newTip = getRandomTip()
    attempts++
  }
  
  return newTip
}

